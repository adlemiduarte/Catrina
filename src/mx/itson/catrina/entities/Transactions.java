package mx.itson.catrina.entities;

import java.util.Date;
import mx.itson.catrina.enums.Type;

/**
 *
 * @author zBook
 */
public class Transactions {
//Define las variables de la clase transaction.
 private Date date; //Define la fecha.
 private String description; //Define la descripción.
 private double amount; //Define amonto como número entero o con decimales.
 private Type type; //Importa la clase Type.
 
 
    /**
     * @return the type
     */
    public Type getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(Type type) {
        this.type = type;
    }


    /**
     * @return the date
     */
    public Date getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(Date date) {
        this.date = date;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the amount
     */
    public double getAmount() {
        return amount;
    }

    /**
     * @param amount the amount to set
     */
    public void setAmount(int amount) {
        this.amount = amount;
    }
 
 
 
}
